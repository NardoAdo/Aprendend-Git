# Solicitando uma string e um número inteiro como entrada. Depois teremos que retornar a string repetida o número de vezes informado.

texto = input("Digite uma string: ")
numero = int(input("Digite um número inteiro: "))

# Retornando a string repetida o número de vezes informado com uma quebra de linha entre cada repetição
resultado = (texto + "\n") * numero
print(resultado)